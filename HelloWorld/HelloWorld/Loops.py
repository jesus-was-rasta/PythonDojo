#Loops

for steps in range (4):
    print(steps)

#parte a contare da 1
for steps in range (1,4):
    print(steps)

#parte a contare da 1, prende un valore ogni 2, vengono quindi saltati i numeri pari
for steps in range (1,10,2):
    print(steps)

#gli do io i valori
for steps in range [1,2,5,7]:
    print(steps)
